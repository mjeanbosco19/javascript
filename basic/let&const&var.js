// let, const and var

/* 
with let keyword we can declare variable,
and we can reassigning a value to the valiable. 
*/

let age = 22; // called a mutable variable
age = 30;

/* 
with const keyword we can create variable,
that we can not reassign. 
*/

const birthYear = 2000;  // called an immutable variable
